//-------- Escape Sequences --------
// Escape sequences are special characters used to modify our code. It starts with '\'. Some escape sequences are :

//1. \n - To jump in the new line
console.log("Hello!\nMyself Zayn Mirza");

//2. \t - To insert horizontal tabs (extra spaces)
console.log("Hello! \tMyself Zayn Mirza");

//3. \v - To insert vertical tabs
console.log("Hello! \vMyself Zayn Mirza");

//4. \ - To insert something in the string
console.log("My Name is 'Zayn\""); // to insert quotes between string
console.log("Mountains : /\\/\\/\\/\\"); //to insert backslash to a string
console.log("Multiple backslash : \\\\\\\\\\");
console.log("To insert tabs : \\t"); // to cancel the escape sequence

//5. \r - To combine the string
console.log("HelloWorldImZaynMirza\r1234567890");

//6. \f -  To formfeed the string
console.log("Hello\fPython");

//7. \b - To set backspace
console.log("Hello \bWorl\bd");

//8. \u - For Unicodes
console.log("\u{1F600}");
